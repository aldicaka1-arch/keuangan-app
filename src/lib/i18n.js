const ASSET_TYPES_ID = ["Tanah", "Rumah", "Mobil", "Motor", "Properti Lain", "Emas/Perhiasan", "Lainnya"];

const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(() => {
    const saved = localStorage.getItem(LANG_KEY) || "id";
    document.documentElement.lang = saved === "en" ? "en" : "id";
    return saved;
  });

  const setLang = useCallback((l) => {
    localStorage.setItem(LANG_KEY, l);
    setLangState(l);
    document.documentElement.lang = l === "en" ? "en" : "id";
  }, []);

  const t = useCallback((key) => STRINGS[lang]?.[key] ?? STRINGS.id[key] ?? key, [lang]);

  const label = useCallback((name) => {
    if (!name || lang === "id") return name;
    return CAT_LABELS.en[name] ?? name;
  }, [lang]);

  const locale = lang === "en" ? "en-US" : "id-ID";

  const fmtDate = useCallback((s) => {
    if (!s) return "";
    return new Date(s + "T00:00:00").toLocaleDateString(locale, { day: "2-digit", month: "short", year: "numeric" });
  }, [locale]);

  const assetTypes = useMemo(() => ASSET_TYPES_ID.map((j) => ({ id: j, label: label(j) })), [label]);

  const value = useMemo(() => ({ lang, setLang, t, label, fmtDate, locale, assetTypes }), [lang, setLang, t, label, fmtDate, locale, assetTypes]);

  return createElement(I18nContext.Provider, { value }, children);
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
